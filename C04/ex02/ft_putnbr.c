#include <unistd.h>

void	ft_putnbr(int nb)
{
	unsigned int	n;
	int				a;

	n = nb;
	if (nb < 0)
	{
		write(1, "-", 1);
		n = nb * -1;
	}
	if (n < 10)
	{
		a = n + 48;
		write(1, &a, 1);
	}
	if (n >= 10)
	{
		ft_putnbr(n / 10);
		ft_putnbr(n % 10);
	}
}
